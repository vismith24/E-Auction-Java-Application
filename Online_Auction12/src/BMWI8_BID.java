    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String bid_=jTextField1.getText();
        int bid;
        if(bid_.isEmpty())
            {
                JOptionPane.showMessageDialog(this,"Enter your bid amount");
            }
        else
        {
        bid=Integer.parseInt(bid_);
        if(bid<25000000)
        {
            JOptionPane.showMessageDialog(this,"Enter Bid more than 2,50,00,000");
        }
        else
        {
        String username=jTextField2.getText();
            if(username.isEmpty())
            {
                JOptionPane.showMessageDialog(this,"Please Confirm your Username");
            }
            else
            {
         try
        {
            Class.forName("java.sql.DriverManager");
            Connection con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/E_AUCTION","root","root");
            Statement stmt=(Statement)con.createStatement();
            String sql1="Select * from Users where UserName='"+username+"';";
            ResultSet rs1=stmt.executeQuery(sql1);
            if(rs1.first())
            {
                String name=rs1.getString("Name");
                String phone=rs1.getString("PhoneNo");
                String city=rs1.getString("City");
            String sql2="Insert into BMW_bid values('"+name+"','"+username+"','"+phone+"','"+city+"','"+bid+"');";
            stmt.executeUpdate(sql2);
            JOptionPane.showMessageDialog(this,"BID PLACED SUCCESSFULLY");
            new ThankYOU().setVisible(true);
            this.setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(this,"NO SUCH USERNAME");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
            }
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

